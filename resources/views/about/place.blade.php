<h3 class="section-title">5. नसीरुद्दीन मेमोरियल हॉल</h3>
<div class="card shadow-sm mb-4">
<div class="card-body">

<p>
नसीरुद्दीन मेमोरियल हॉल (पूर्व नाम – विलॉबी हॉल) का इतिहास भारतीय स्वतंत्रता संग्राम से जुड़ा हुआ है।
</p>

<p><strong>ऐतिहासिक पृष्ठभूमि:</strong> 26 अगस्त 1920 को खिलाफत आंदोलन से प्रेरित क्रांतिकारियों – नसीरुद्दीन 'मौजी', बशीरुद्दीन और मासूम अली – ने तत्कालीन ब्रिटिश कलेक्टर आर.डब्ल्यू.डी. विलॉबी की हत्या की थी।</p>

<p><strong>निर्माण:</strong> इस घटना के बाद 1924 में अंग्रेजों द्वारा विलॉबी मेमोरियल हॉल का निर्माण कराया गया।</p>

<p><strong>पुनः नामकरण:</strong> वर्ष 1997 में जिला प्रशासन द्वारा इसका नाम बदलकर नसीरुद्दीन मेमोरियल हॉल रखा गया।</p>


<h3 class="section-title">6. राजा लोन सिंह की गढ़ी (मितौली)</h3>
<div class="card shadow-sm mb-4">
<div class="card-body">

<p>
मितौली के राजा लोन सिंह 1857 के प्रथम स्वतंत्रता संग्राम के वीर योद्धा थे।
उनकी गढ़ी (किला) विद्रोह का प्रमुख केंद्र थी।
</p>

<p><strong>स्थान:</strong> यह किला मितौली तहसील में स्थित था और घने जंगलों से घिरा एक अभेद्य दुर्ग माना जाता था।</p>

<p><strong>1857 में भूमिका:</strong> राजा लोन सिंह ने बेगम हजरत महल के प्रति निष्ठा व्यक्त करते हुए अंग्रेजों के विरुद्ध युद्ध का नेतृत्व किया।</p>

<p><strong>विनाश:</strong> 1858 में अंग्रेजी सेना ने किले को तोपों से ध्वस्त कर दिया ताकि वह भविष्य में विद्रोह का केंद्र न बन सके।</p>

<p><strong>वर्तमान स्थिति:</strong> आज केवल अवशेष शेष हैं, किंतु स्थानीय लोग इस स्थान को श्रद्धा की दृष्टि से देखते हैं। यहाँ एक प्राचीन शिव मंदिर एवं पवित्र सरोवर भी स्थित है।</p>

